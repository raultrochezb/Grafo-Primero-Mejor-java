
package Models;

public class Arista {
    private Arista Sig;
    private Vertice Ady;
    private double Precio;

    public Arista getSig() {
        return Sig;
    }

    public void setSig(Arista Sig) {
        this.Sig = Sig;
    }

    public Vertice getAdy() {
        return Ady;
    }

    public void setAdy(Vertice Ady) {
        this.Ady = Ady;
    }

    public double getPrecio() {
        return Precio;
    }

    public void setPrecio(double Precios) {
        this.Precio = Precios;
    }
 
}
